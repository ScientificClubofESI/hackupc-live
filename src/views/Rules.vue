                Teams can be disqualified from the competition at the organizers' discretion. Reasons might include but are not limited to breaking the Competition Rules, breaking the <a href="https://github.com/hackupc/rules/blob/master/CodeofConduct.md">Code of Conduct</a>, or other unsporting behaviour.
              </li>
